export type SourceRegistryId = keyof typeof sourceRegistry;

export function getSourceMetadata(sourceId: string) {
  return sourceRegistry[sourceId as SourceRegistryId] ?? null;
}

export function getConfidenceLabel(confidence: string) {
  switch (confidence) {
    case 'official':
      return 'Official';
    case 'reference':
      return 'Reference';
    case 'approximate':
      return 'Approximate';
    case 'experimental':
      return 'Experimental';
    default:
      return 'Unavailable';
  }
}
